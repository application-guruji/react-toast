// Utility function to get icon by type
export const getToastIcon = (type: string) => {
  switch (type) {
    case 'success':
      return CheckCircleIcon;
    case 'danger':
      return XCircleIcon;
    case 'warning':
      return AlertTriangleIcon;
    case 'info':
      return AlertCircleIcon;
    case 'default':
    default:
      return DefaultIcon;
  }
};

